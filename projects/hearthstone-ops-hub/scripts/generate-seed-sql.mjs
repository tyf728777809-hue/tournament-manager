#!/usr/bin/env node
import fs from 'node:fs'
import path from 'node:path'

const scriptDir = path.dirname(new URL(import.meta.url).pathname)
const projectRoot = path.resolve(scriptDir, '..')
const userId = process.argv[2]
const outputArg = process.argv[3] || 'seed-import.generated.sql'
const outputPath = path.isAbsolute(outputArg) ? outputArg : path.join(projectRoot, outputArg)

if (!userId) {
  console.error('用法: node scripts/generate-seed-sql.mjs <SUPABASE_USER_ID> [output.sql]')
  process.exit(1)
}

const eventsPath = path.join(projectRoot, 'seed-events-v1.template.json')
const tasksPath = path.join(projectRoot, 'seed-tasks-v1.template.json')
const dailyPath = path.join(projectRoot, 'seed-daily-checklists-v1.template.json')

for (const p of [eventsPath, tasksPath, dailyPath]) {
  if (!fs.existsSync(p)) {
    console.error(`缺少模板文件: ${p}`)
    process.exit(1)
  }
}

const events = JSON.parse(fs.readFileSync(eventsPath, 'utf8'))
const tasks = JSON.parse(fs.readFileSync(tasksPath, 'utf8'))
const dailyChecklists = JSON.parse(fs.readFileSync(dailyPath, 'utf8'))

const q = (value) => {
  if (value === null || value === undefined) return 'null'
  return `'${String(value).replaceAll("'", "''")}'`
}

const qDate = (value) => {
  if (!value) return 'null'
  return `date ${q(value)}`
}

const sql = []
sql.push('-- Generated by scripts/generate-seed-sql.mjs')
sql.push(`-- user_id: ${userId}`)
sql.push('begin;')
sql.push('')

sql.push('-- 1) hs_events')
sql.push('insert into public.hs_events (user_id, title, slug, type, priority, status, start_date, end_date, description, source_doc, sort_order) values')
sql.push(
  events
    .map(
      (item) =>
        `  (${q(userId)}, ${q(item.title)}, ${q(item.slug)}, ${q(item.type)}, ${q(item.priority)}, ${q(item.status)}, ${q(item.start_date)}, ${q(item.end_date)}, ${q(item.description)}, ${q(item.source_doc)}, ${item.sort_order ?? 0})`,
    )
    .join(',\n') + ';',
)
sql.push('')

sql.push('-- 2) hs_tasks')
sql.push(`insert into public.hs_tasks (user_id, event_id, title, task_type, priority, status, task_date, planned_date, due_date, source_type, notes)
select
  ${q(userId)},
  e.id,
  v.title,
  v.task_type,
  v.priority,
  v.status,
  v.task_date,
  v.planned_date,
  v.due_date,
  v.source_type,
  v.notes
from (values`)
sql.push(
  tasks
    .map(
      (item) =>
        `  (${q(item.event_slug)}, ${q(item.title)}, ${q(item.task_type)}, ${q(item.priority)}, ${q(item.status)}, ${qDate(item.task_date)}, ${qDate(item.planned_date)}, ${qDate(item.due_date)}, ${q(item.source_type)}, ${q(item.notes)})`,
    )
    .join(',\n'),
)
sql.push(`) as v(event_slug, title, task_type, priority, status, task_date, planned_date, due_date, source_type, notes)
left join public.hs_events e
  on e.slug = v.event_slug
 and e.user_id = ${q(userId)};`)
sql.push('')

sql.push('-- 3) hs_daily_checklists')
sql.push(`insert into public.hs_daily_checklists (user_id, related_event_id, check_date, title, content, priority, status, source_type, sort_order)
select
  ${q(userId)},
  e.id,
  v.check_date,
  v.title,
  v.content,
  v.priority,
  v.status,
  v.source_type,
  v.sort_order
from (values`)
sql.push(
  dailyChecklists
    .map(
      (item) =>
        `  (${q(item.related_event_slug)}, ${qDate(item.check_date)}, ${q(item.title)}, ${q(item.content)}, ${q(item.priority)}, ${q(item.status)}, ${q(item.source_type)}, ${item.sort_order ?? 0})`,
    )
    .join(',\n'),
)
sql.push(`) as v(related_event_slug, check_date, title, content, priority, status, source_type, sort_order)
left join public.hs_events e
  on e.slug = v.related_event_slug
 and e.user_id = ${q(userId)};`)
sql.push('')
sql.push('commit;')
sql.push('')

fs.writeFileSync(outputPath, sql.join('\n'))
console.log(`✅ 已生成: ${outputPath}`)
